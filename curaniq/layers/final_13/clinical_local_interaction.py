@dataclass
class SDOHScreeningResult:
    domain: str
    positive: bool
    screening_loinc: str
    icd10_codes: list[str] = field(default_factory=list)
    drug_implications: list[str] = field(default_factory=list)


class SDOHGravityIntegration:
    """
    L7-5: Social Determinants of Health screening and drug impact assessment.
    SDOH domains from curaniq/data/sdoh_gravity_profiles.json.

    When SDOH screening data is available (via FHIR Observation),
    this engine flags drug implications:
    - Food insecurity → insulin timing, temperature-sensitive storage
    - Housing instability → medication storage, adherence barriers
    - Financial strain → cost-driven non-adherence, generic preference
    - Health literacy → simplified education materials needed

    Source: HL7 FHIR Gravity Project; ADA 2024 §1.6; NASEM 2019
    """

    def __init__(self):
        from curaniq.data_loader import load_json_data
        raw = load_json_data("sdoh_gravity_profiles.json")
        self._domains = raw.get("domains", {})
        logger.info("SDOHGravityIntegration: %d SDOH domains", len(self._domains))

    def assess_drug_implications(self, sdoh_positive_domains: list[str],
                                  drugs: list[str]) -> list[dict]:
        """Assess how SDOH factors affect medication management."""
        implications = []

        for domain_key in sdoh_positive_domains:
            domain = self._domains.get(domain_key)
            if not domain:
                continue

            for implication in domain.get("drug_implications", []):
                implications.append({
                    "sdoh_domain": domain_key,
                    "implication": implication,
                    "icd10_codes": domain.get("icd10_codes", []),
                    "screening_loinc": domain.get("screening_loinc", ""),
                    "affected_drugs": drugs,  # All drugs potentially affected
                })

        return implications

    def get_screening_codes(self) -> list[dict]:
        """Get LOINC screening codes for all SDOH domains."""
        return [
            {"domain": k, "loinc": v.get("screening_loinc", ""),
             "icd10": v.get("icd10_codes", [])}
            for k, v in self._domains.items()
        ]


