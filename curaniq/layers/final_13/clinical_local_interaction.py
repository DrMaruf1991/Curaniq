class NoteSection(str, Enum):
    CHIEF_COMPLAINT  = "chief_complaint"
    ASSESSMENT       = "assessment"
    PLAN             = "plan"
    MEDICATIONS      = "medications"
    EVIDENCE_SUMMARY = "evidence_summary"
    SAFETY_ALERTS    = "safety_alerts"


class ClinicalDocumentationAssistant:
    """
    L7-12: Generates structured clinical notes from CURANIQ interactions.

    Transforms the pipeline output (query → evidence → claims → safety)
    into a structured clinical note following SOAP-like format.

    Output: FHIR DocumentReference-compatible JSON.
    The note is DETERMINISTIC — assembled from pipeline outputs,
    NOT generated by LLM. Every sentence traces to a pipeline component.
    """

    def generate_note(self, query_text: str, drugs: list[str],
                      conditions: list[str], summary: str,
                      safety_alerts: list[str],
                      evidence_count: int,
                      clinician_id: str = "") -> dict:
        """Generate a structured clinical note from pipeline outputs."""
        now = datetime.now(timezone.utc)

        sections = []

        # Assessment section
        if conditions:
            sections.append({
                "section": NoteSection.ASSESSMENT.value,
                "text": f"Clinical query regarding: {', '.join(conditions)}. "
                        f"Medications under review: {', '.join(drugs)}.",
            })

        # Evidence summary
        if summary:
            sections.append({
                "section": NoteSection.EVIDENCE_SUMMARY.value,
                "text": f"CURANIQ evidence synthesis based on {evidence_count} source(s). "
                        f"{summary[:500]}",
            })

        # Safety alerts
        if safety_alerts:
            alert_text = "; ".join(safety_alerts[:10])
            sections.append({
                "section": NoteSection.SAFETY_ALERTS.value,
                "text": f"Safety alerts ({len(safety_alerts)}): {alert_text}",
            })

        # Medications
        if drugs:
            sections.append({
                "section": NoteSection.MEDICATIONS.value,
                "text": f"Medications reviewed: {', '.join(drugs)}",
            })

        return {
            "resource_type": "DocumentReference",
            "status": "current",
            "type": {"coding": [{"system": "http://loinc.org", "code": "11506-3",
                                  "display": "Progress note"}]},
            "date": now.isoformat(),
            "author": clinician_id or "CURANIQ-MEOS",
            "content": sections,
            "generated_by": "CURANIQ Clinical Documentation Assistant (L7-12)",
            "note": "This note was assembled deterministically from pipeline outputs. "
                    "It is NOT LLM-generated prose.",
        }


