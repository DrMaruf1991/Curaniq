@dataclass
class DischargeChecklistItem:
    category: str
    item: str
    completed: bool = False
    responsible: str = ""
    notes: str = ""


class DischargePlanningModule:
    """
    L7-13: Manages discharge medication reconciliation and care transitions.

    Checklist-driven discharge process:
    1. Medication reconciliation (L7-8) at discharge
    2. High-risk drug counseling (anticoagulants, insulin, opioids)
    3. Follow-up appointments scheduled
    4. Patient education materials generated (L8-9)
    5. GP/primary care handoff letter generated

    Source: NICE NG27 Transition between settings; Joint Commission NPSG.02.03.01
    """

    HIGH_RISK_CATEGORIES: set[str] = {
        "anticoagulant", "insulin", "opioid", "immunosuppressant",
        "chemotherapy", "narrow_therapeutic_index",
    }

    def generate_checklist(self, patient_drugs: list[dict],
                           conditions: list[str]) -> list[DischargeChecklistItem]:
        """Generate discharge checklist based on patient's medications."""
        checklist = [
            DischargeChecklistItem("medication_reconciliation",
                "Complete admission vs discharge medication reconciliation",
                responsible="pharmacist"),
            DischargeChecklistItem("medication_reconciliation",
                "Document and explain all medication CHANGES (new, stopped, dose changed)",
                responsible="doctor"),
        ]

        # High-risk drug-specific counseling
        for drug_info in patient_drugs:
            drug_name = drug_info.get("drug", "")
            drug_class = drug_info.get("class", "").lower()
            if drug_class in self.HIGH_RISK_CATEGORIES or drug_name.lower() in (
                "warfarin", "rivaroxaban", "apixaban", "insulin", "methotrexate",
                "lithium", "clozapine", "oxycodone", "morphine",
            ):
                checklist.append(DischargeChecklistItem(
                    "high_risk_counseling",
                    f"Counsel patient on {drug_name}: dosing, side effects, monitoring, when to seek help",
                    responsible="pharmacist/nurse",
                ))

        # Standard items
        checklist.extend([
            DischargeChecklistItem("follow_up",
                "Schedule follow-up appointment within 7-14 days",
                responsible="admin"),
            DischargeChecklistItem("follow_up",
                "Ensure GP letter sent within 24 hours of discharge",
                responsible="doctor"),
            DischargeChecklistItem("patient_education",
                "Provide written medication list in patient's language",
                responsible="nurse"),
            DischargeChecklistItem("patient_education",
                "Explain red flag symptoms requiring emergency re-attendance",
                responsible="doctor/nurse"),
            DischargeChecklistItem("supply",
                "Ensure minimum 7-day medication supply provided",
                responsible="pharmacy"),
        ])

        return checklist

    def generate_handoff_letter(self, patient_name: str, conditions: list[str],
                                drugs_on_discharge: list[dict],
                                changes_made: list[str],
                                follow_up: str = "") -> dict:
        """Generate GP/primary care handoff letter."""
        return {
            "type": "discharge_summary_letter",
            "to": "Primary Care Provider",
            "patient": patient_name,
            "date": datetime.now(timezone.utc).isoformat(),
            "diagnoses": conditions,
            "medications_on_discharge": [d.get("drug", "") for d in drugs_on_discharge],
            "changes_during_admission": changes_made,
            "follow_up_required": follow_up or "Review in 7-14 days",
            "generated_by": "CURANIQ Discharge Planning (L7-13)",
        }


