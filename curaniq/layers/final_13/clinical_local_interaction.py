@dataclass
class ProactiveCDSCard:
    card_id: str = field(default_factory=lambda: str(uuid4()))
    trigger: str = ""      # What EHR event triggered this
    summary: str = ""
    detail: str = ""
    indicator: str = ""    # "critical", "warning", "info"
    source_module: str = ""
    actions: list[dict] = field(default_factory=list)
    auto_dismissed: bool = False


class ZeroClickProactiveCDS:
    """
    L14-11: Push-based CDS that fires WITHOUT clinician query.

    Listens for EHR events (via CDS Hooks L7-2 or FHIR Subscription)
    and proactively generates evidence cards when:
    1. New lab result triggers clinical action (e.g., K+ 6.2 + spironolactone)
    2. New medication order has safety concern (DDI, allergy)
    3. Guideline update affects current patient's treatment
    4. Clinical score threshold crossed (NEWS2 >= 7)

    Cards are ranked by urgency. Critical cards cannot be auto-dismissed.
    Connected to: L7-4 ContextAwareEvidenceDelivery, L7-10 AlertFatigueManager.
    """

    def __init__(self):
        self._pending_cards: list[ProactiveCDSCard] = []

    def on_lab_result(self, lab_name: str, value: float,
                      patient_drugs: list[str]) -> Optional[ProactiveCDSCard]:
        """React to a new lab result — generate proactive card if needed."""
        # Use L7-11 LabResultInterpreter logic
        critical_pairs = {
            "potassium": {"high": 5.5, "drugs": ["spironolactone", "amiloride", "enalapril", "lisinopril", "losartan"]},
            "inr": {"high": 4.0, "drugs": ["warfarin"]},
            "creatinine": {"high": 300, "drugs": ["vancomycin", "gentamicin", "metformin", "methotrexate"]},
            "glucose": {"low": 3.0, "drugs": ["insulin", "glibenclamide", "glimepiride"]},
        }

        lab_lower = lab_name.lower()
        drugs_lower = {d.lower() for d in patient_drugs}

        for lab_key, config in critical_pairs.items():
            if lab_key not in lab_lower:
                continue

            if "high" in config and value >= config["high"]:
                culprits = drugs_lower & {d.lower() for d in config["drugs"]}
                if culprits:
                    card = ProactiveCDSCard(
                        trigger=f"Lab: {lab_name}={value}",
                        summary=f"CRITICAL: {lab_name} {value} with {', '.join(culprits)}",
                        detail=f"{lab_name} is critically elevated. Patient is on {', '.join(culprits)} "
                               f"which may be contributing. Immediate review recommended.",
                        indicator="critical",
                        source_module="L14-11",
                        actions=[{"type": "review_medication", "drugs": list(culprits)},
                                 {"type": "order_repeat_lab", "lab": lab_name}],
                    )
                    self._pending_cards.append(card)
                    return card

            if "low" in config and value <= config["low"]:
                culprits = drugs_lower & {d.lower() for d in config["drugs"]}
                if culprits:
                    card = ProactiveCDSCard(
                        trigger=f"Lab: {lab_name}={value}",
                        summary=f"CRITICAL: {lab_name} {value} with {', '.join(culprits)}",
                        detail=f"{lab_name} critically low. {', '.join(culprits)} may be responsible.",
                        indicator="critical",
                        source_module="L14-11",
                        actions=[{"type": "hold_medication", "drugs": list(culprits)}],
                    )
                    self._pending_cards.append(card)
                    return card

        return None

    def on_medication_order(self, new_drug: str, current_drugs: list[str],
                             patient_allergies: list[str]) -> Optional[ProactiveCDSCard]:
        """React to a new medication order — proactive safety check."""
        new_lower = new_drug.lower()

        # Allergy check
        for allergy in patient_allergies:
            if allergy.lower() in new_lower or new_lower in allergy.lower():
                card = ProactiveCDSCard(
                    trigger=f"New order: {new_drug}",
                    summary=f"ALLERGY ALERT: {new_drug} — patient has documented allergy to {allergy}",
                    indicator="critical",
                    source_module="L14-11",
                    actions=[{"type": "cancel_order", "drug": new_drug}],
                )
                self._pending_cards.append(card)
                return card

        return None

    def get_pending_cards(self) -> list[ProactiveCDSCard]:
        """Get all pending proactive CDS cards, sorted by urgency."""
        priority = {"critical": 0, "warning": 1, "info": 2}
        return sorted(self._pending_cards,
                       key=lambda c: priority.get(c.indicator, 3))

    def dismiss_card(self, card_id: str) -> bool:
        """Dismiss a card. Critical cards cannot be auto-dismissed."""
        card = next((c for c in self._pending_cards if c.card_id == card_id), None)
        if not card:
            return False
        if card.indicator == "critical":
            return False  # Cannot dismiss critical alerts
        self._pending_cards = [c for c in self._pending_cards if c.card_id != card_id]
        return True
