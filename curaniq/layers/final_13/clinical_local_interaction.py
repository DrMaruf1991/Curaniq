class ConsentState(str, Enum):
    NOT_OBTAINED   = "not_obtained"
    VERBAL_CONSENT = "verbal_consent"
    WRITTEN_CONSENT = "written_consent"
    DECLINED       = "declined"


class AmbientConsultationMonitor:
    """
    L14-10: Manages continuous ambient audio monitoring during consultations.

    Extends L12-8 AmbientAudioSentinel with:
    - Consent state machine (must be obtained before recording starts)
    - Real-time streaming to L14-9 Voice Pipeline
    - Live alert overlay (safety phrases trigger immediate UI notification)
    - Session lifecycle (start → monitor → pause → stop → summarize)

    Audio is NEVER stored by CURANIQ. Processed in real-time, discarded.
    Only structured outputs (safety flags, medication orders) are retained.
    HIPAA: PHI scrubbed from all retained data via L6-2.
    """

    def __init__(self):
        self._sessions: dict[str, dict] = {}

    def start_session(self, session_id: str, clinician_id: str,
                      consent_state: ConsentState) -> dict:
        """Start an ambient monitoring session."""
        if consent_state in (ConsentState.NOT_OBTAINED, ConsentState.DECLINED):
            return {
                "started": False,
                "error": "Patient consent required before ambient monitoring. "
                         f"Current consent state: {consent_state.value}",
            }

        session = {
            "session_id": session_id,
            "clinician_id": clinician_id,
            "consent": consent_state.value,
            "started_at": datetime.now(timezone.utc).isoformat(),
            "status": "active",
            "safety_flags": [],
            "medication_orders_detected": [],
        }
        self._sessions[session_id] = session
        return {"started": True, "session": session}

    def process_audio_chunk(self, session_id: str, transcript_chunk: str) -> dict:
        """Process a real-time audio transcript chunk."""
        session = self._sessions.get(session_id)
        if not session or session["status"] != "active":
            return {"processed": False, "error": "No active session"}

        # Delegate to L12-8 for safety analysis
        from curaniq.layers.L12_research.research_genomics import AmbientAudioSentinel
        sentinel = AmbientAudioSentinel()
        analysis = sentinel.analyze_transcript(transcript_chunk)

        # Accumulate flags
        session["safety_flags"].extend(analysis.get("safety_flags", []))

        return {
            "processed": True,
            "real_time_alerts": [f for f in analysis.get("safety_flags", [])
                                 if f.get("urgency") == "critical"],
        }

    def stop_session(self, session_id: str) -> dict:
        """Stop an ambient monitoring session and generate summary."""
        session = self._sessions.get(session_id)
        if not session:
            return {"error": "Session not found"}

        session["status"] = "completed"
        session["ended_at"] = datetime.now(timezone.utc).isoformat()

        return {
            "session_id": session_id,
            "total_safety_flags": len(session["safety_flags"]),
            "medication_orders": session["medication_orders_detected"],
            "status": "completed",
        }


