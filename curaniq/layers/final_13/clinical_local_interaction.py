class CurrencyCostEngine:
    """
    L11-3: Multi-currency drug cost awareness.
    Reference prices from curaniq/data/drug_cost_reference.json.

    Provides cost context for prescribing decisions:
    - "This drug costs ~$X/month in your region"
    - "Generic alternative saves ~$Y/month"
    - Cost tier classification (low/moderate/high/very high)

    Exchange rates updated from env: CURANIQ_EXCHANGE_RATE_API
    Fallback: static rates from data file.
    """

    def __init__(self):
        from curaniq.data_loader import load_json_data
        raw = load_json_data("drug_cost_reference.json")
        self._reference_prices = raw.get("reference_prices_usd", {})
        self._exchange_rates = raw.get("exchange_rates_per_usd", {})
        self._cost_tiers = raw.get("cost_tiers", {})
        logger.info("CurrencyCostEngine: %d reference prices, %d currencies",
                     len(self._reference_prices), len(self._exchange_rates))

    def estimate_monthly_cost(self, drug_name: str, daily_doses: int = 1,
                               currency: str = "USD") -> dict:
        """Estimate monthly cost for a drug."""
        drug_key = drug_name.lower().replace(" ", "_")

        # Find matching reference price
        unit_price_usd = None
        for key, price in self._reference_prices.items():
            if drug_key in key or key in drug_key:
                unit_price_usd = price
                break

        if unit_price_usd is None:
            return {"drug": drug_name, "cost_available": False}

        monthly_usd = unit_price_usd * daily_doses * 30
        exchange = self._exchange_rates.get(currency, 1.0)
        monthly_local = monthly_usd * exchange

        # Classify tier
        tier = "unknown"
        for tier_name, tier_info in self._cost_tiers.items():
            if monthly_usd <= tier_info.get("max_usd_per_month", 99999):
                tier = tier_info.get("label", tier_name)
                break

        return {
            "drug": drug_name,
            "cost_available": True,
            "unit_price_usd": round(unit_price_usd, 4),
            "monthly_cost_usd": round(monthly_usd, 2),
            "monthly_cost_local": round(monthly_local, 2),
            "currency": currency,
            "cost_tier": tier,
            "note": "WHO/HAI reference prices. Actual costs may vary by supplier and region.",
        }


