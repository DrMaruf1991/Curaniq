# L11-2: SUPPLY CHAIN PRESCRIBING (Drug Shortage Awareness)
# API: FDA Drug Shortage Database (verified: accessdata.fda.gov)
# =============================================================================

class SupplyChainPrescribing:
    """
    L11-2: Real-time drug shortage awareness for prescribing decisions.

    Checks FDA Drug Shortages database + local availability (L11-1).
    When a drug is in shortage, suggests therapeutic alternatives.

    API: https://api.fda.gov/drug/drugsfda.json (OpenFDA)
    Local: DMED/MEDPLUS registry for Uzbekistan via L11-1.
    """

    FDA_SHORTAGE_URL = "https://api.fda.gov/drug/drugsfda.json"

    def __init__(self):
        self._api_key = os.environ.get("OPENFDA_API_KEY", "")

    def check_shortage(self, drug_name: str) -> dict:
        """Check if a drug is currently in shortage via FDA."""
        params = {
            "search": f'openfda.generic_name:"{drug_name}"',
            "limit": "1",
        }
        if self._api_key:
            params["api_key"] = self._api_key

        try:
            url = self.FDA_SHORTAGE_URL + "?" + urllib.parse.urlencode(params)
            req = urllib.request.Request(url, headers={"User-Agent": "CURANIQ/1.0"})
            with urllib.request.urlopen(req, timeout=10) as resp:
                data = json.loads(resp.read().decode())
                results = data.get("results", [])
                if results:
                    return {
                        "drug": drug_name,
                        "found_in_fda": True,
                        "shortage_status": "check_ashp",  # FDA doesn't have real-time shortage in this API
                        "note": "For real-time shortage data, check ASHP Drug Shortage Resource Center",
                        "ashp_url": "https://www.ashp.org/drug-shortages",
                    }
                return {"drug": drug_name, "found_in_fda": False, "shortage_status": "unknown"}
        except Exception as e:
            return {"drug": drug_name, "error": str(e)}

    def suggest_alternatives(self, drug_name: str, drug_class: str = "") -> list[str]:
        """Suggest therapeutic alternatives for a drug in shortage."""
        # This would integrate with L3-1 CQL kernel for therapeutic equivalents
        return [f"Consult formulary for {drug_class or drug_name} alternatives",
                "Check L11-1 local drug availability for Uzbekistan/CIS alternatives"]


