"""
CURANIQ — Medical Evidence Operating System
Layer 4: Adversarial LLM Cross-Verification (L4-12) + Confidence Scorer (L4-13)

L4-12 — Adversarial LLM Cross-Verification (Jury Protocol):
'After the Constrained Generator produces draft claims from evidence, this module
runs adversarial verification. The verifier NEVER generates alternative answers —
it only critiques. Critiquing is more reliable than generating.'

Verification flow:
1. PRIMARY path — Claude generates structured claims from evidence IDs
2. VERIFIER path — GPT-4o receives SAME evidence + Claude's claims
   → Asked ONLY to critique: 'Does each claim faithfully represent the evidence?'
   → Flag: distortions, omissions, scope misapplications, unsupported inferences
3. TIE-BREAKING — if verifier flags → NLI model (SciFact/MedNLI) breaks tie
   (a) NLI agrees with verifier → claim SUPPRESSED
   (b) NLI agrees with primary → claim PASSES with uncertainty marker
4. THRESHOLD — if >30% of claims in high-risk query flagged → REFUSE + escalate

L4-13 — Confidence Scoring & Uncertainty Quantifier:
Full composite score = weighted average of:
(a) NLI entailment score (0.0-1.0)
(b) Cross-LLM agreement from L4-12
(c) Evidence quality per Oxford CEBM
(d) Evidence recency
(e) Source count
"""

