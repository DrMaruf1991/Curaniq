@dataclass
class OutcomeRecord:
    record_id: str = field(default_factory=lambda: str(uuid4()))
    query_id: str = ""
    patient_id_hash: str = ""  # SHA-256 of patient ID (never store actual ID)
    curaniq_recommendation: str = ""
    actual_outcome: str = ""
    outcome_positive: bool = True
    days_to_outcome: int = 0
    recorded_at: datetime = field(default_factory=lambda: datetime.now(timezone.utc))


class ClinicalOutcomeTracker:
    """
    L10-7: Tracks whether CURANIQ recommendations led to good outcomes.

    Workflow:
    1. CURANIQ generates recommendation (logged in L9-1)
    2. Clinician accepts/modifies (logged in L9-4)
    3. Patient outcome recorded (this module) — days/weeks later
    4. Outcome linked back to recommendation for quality analysis

    Patient ID is SHA-256 hashed — CURANIQ never stores actual identifiers.
    """

    def __init__(self):
        self._outcomes: list[OutcomeRecord] = []

    def record_outcome(self, query_id: str, patient_id: str,
                       recommendation: str, outcome: str,
                       positive: bool, days: int = 0) -> OutcomeRecord:
        record = OutcomeRecord(
            query_id=query_id,
            patient_id_hash=hashlib.sha256(patient_id.encode()).hexdigest(),
            curaniq_recommendation=recommendation[:200],
            actual_outcome=outcome[:200],
            outcome_positive=positive,
            days_to_outcome=days,
        )
        self._outcomes.append(record)
        return record

    def get_outcome_stats(self) -> dict:
        total = len(self._outcomes)
        if not total:
            return {"total": 0}
        positive = sum(1 for o in self._outcomes if o.outcome_positive)
        return {
            "total_outcomes": total,
            "positive_rate": round(positive / total, 3),
            "avg_days_to_outcome": round(sum(o.days_to_outcome for o in self._outcomes) / total, 1),
        }


