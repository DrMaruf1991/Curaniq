@dataclass
class ContentCredential:
    credential_id: str = field(default_factory=lambda: str(uuid4()))
    query_id: str = ""
    content_hash: str = ""
    generator: str = "CURANIQ MEOS"
    model_used: str = ""
    evidence_sources_hash: str = ""
    safety_gates_passed: list[str] = field(default_factory=list)
    timestamp: datetime = field(default_factory=lambda: datetime.now(timezone.utc))
    signature: str = ""


class C2PACredentialManager:
    """
    L9-2: Generates C2PA-inspired content credentials for CURANIQ outputs.

    Every clinical response gets a tamper-evident credential containing:
    - SHA-256 hash of the output content
    - Hash of all evidence sources used
    - List of safety gates that passed/failed
    - LLM model identifier
    - HMAC signature (using CURANIQ_C2PA_SECRET)

    Enables: "Was this output actually generated by CURANIQ?" verification.
    """

    def __init__(self):
        self._secret = os.environ.get("CURANIQ_C2PA_SECRET", "").encode()

    def generate_credential(self, query_id: str, output_text: str,
                            model_used: str, evidence_hashes: list[str],
                            gates_passed: list[str]) -> ContentCredential:
        cred = ContentCredential(
            query_id=query_id,
            content_hash=hashlib.sha256(output_text.encode()).hexdigest(),
            model_used=model_used,
            evidence_sources_hash=hashlib.sha256(
                "|".join(sorted(evidence_hashes)).encode()
            ).hexdigest(),
            safety_gates_passed=gates_passed,
        )

        manifest = (
            f"{cred.credential_id}|{cred.query_id}|{cred.content_hash}|"
            f"{cred.evidence_sources_hash}|{cred.timestamp.isoformat()}"
        )
        if self._secret:
            cred.signature = hmac.new(self._secret, manifest.encode(), hashlib.sha256).hexdigest()
        else:
            cred.signature = f"UNSIGNED:{hashlib.sha256(manifest.encode()).hexdigest()[:16]}"

        return cred

    def verify(self, credential: ContentCredential, output_text: str) -> bool:
        content_hash = hashlib.sha256(output_text.encode()).hexdigest()
        if content_hash != credential.content_hash:
            return False
        if not self._secret or credential.signature.startswith("UNSIGNED:"):
            return False
        manifest = (
            f"{credential.credential_id}|{credential.query_id}|{credential.content_hash}|"
            f"{credential.evidence_sources_hash}|{credential.timestamp.isoformat()}"
        )
        expected = hmac.new(self._secret, manifest.encode(), hashlib.sha256).hexdigest()
        return hmac.compare_digest(credential.signature, expected)


