# L9-5: AI SAFETY OFFICER DASHBOARD
# =============================================================================

class AISafetyOfficerDashboard:
    """
    L9-5: Aggregates all safety metrics for governance review.

    Combines data from:
    - L10-4 Benchmark Dashboard (quality metrics)
    - L10-11 Clinician Trust Dashboard (override patterns)
    - L9-4 Override Logger (safety override audit)
    - L5 Safety Gates (gate pass/fail rates)
    - L0-11 Incident System (open incidents)
    """

    def generate_report(self, benchmark_data: dict, trust_data: dict,
                        override_count: int, safety_override_count: int,
                        gate_pass_rates: dict, open_incidents: int) -> dict:
        risk_level = "low"
        if safety_override_count > 5 or open_incidents > 0:
            risk_level = "high"
        elif safety_override_count > 0 or trust_data.get("challenge_rate", 0) > 0.1:
            risk_level = "moderate"

        return {
            "generated_at": datetime.now(timezone.utc).isoformat(),
            "overall_risk_level": risk_level,
            "quality": {
                "benchmark_pass_rate": benchmark_data.get("overall", 0),
                "metrics_measured": benchmark_data.get("measured", 0),
            },
            "clinician_trust": {
                "acceptance_rate": trust_data.get("acceptance_rate", 0),
                "challenge_rate": trust_data.get("challenge_rate", 0),
                "total_interactions": trust_data.get("total_interactions", 0),
            },
            "safety": {
                "total_overrides": override_count,
                "safety_overrides": safety_override_count,
                "gate_pass_rates": gate_pass_rates,
                "open_incidents": open_incidents,
            },
            "action_items": self._generate_actions(risk_level, safety_override_count, open_incidents),
        }

    def _generate_actions(self, risk: str, safety_overrides: int, incidents: int) -> list[str]:
        actions = []
        if incidents > 0:
            actions.append(f"URGENT: {incidents} open incident(s) require resolution.")
        if safety_overrides > 5:
            actions.append(f"REVIEW: {safety_overrides} safety gate overrides. Investigate root causes.")
        if risk == "high":
            actions.append("ESCALATE: Overall risk level HIGH. Schedule safety review meeting.")
        return actions


