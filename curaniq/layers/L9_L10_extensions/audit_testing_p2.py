class FeedbackLoopAnalytics:
    """
    L10-8: Analyzes clinician feedback patterns for system improvement.

    Aggregates from L10-11 Clinician Trust Dashboard + L8-7 Challenge Button:
    - Which claim types get challenged most?
    - Which drug classes have lowest acceptance?
    - Are specific clinicians consistently disagreeing?
    - Time-series: is trust improving or degrading?
    """

    def __init__(self):
        self._feedback: list[dict] = []

    def record_feedback(self, query_id: str, clinician_id: str,
                        feedback_type: str, claim_type: str = "",
                        drug_class: str = "", comment: str = ""):
        self._feedback.append({
            "query_id": query_id, "clinician_id": clinician_id,
            "type": feedback_type, "claim_type": claim_type,
            "drug_class": drug_class, "comment": comment[:200],
            "timestamp": datetime.now(timezone.utc).isoformat(),
        })

    def get_improvement_signals(self) -> dict:
        if not self._feedback:
            return {"total": 0}

        by_type: dict[str, int] = {}
        by_claim: dict[str, int] = {}
        by_drug: dict[str, int] = {}
        challenges = 0

        for fb in self._feedback:
            by_type[fb["type"]] = by_type.get(fb["type"], 0) + 1
            if fb["claim_type"]:
                by_claim[fb["claim_type"]] = by_claim.get(fb["claim_type"], 0) + 1
            if fb["drug_class"]:
                by_drug[fb["drug_class"]] = by_drug.get(fb["drug_class"], 0) + 1
            if fb["type"] == "challenge":
                challenges += 1

        return {
            "total_feedback": len(self._feedback),
            "challenge_rate": round(challenges / len(self._feedback), 3) if self._feedback else 0,
            "most_challenged_claim_types": dict(sorted(by_claim.items(), key=lambda x: -x[1])[:5]),
            "most_challenged_drug_classes": dict(sorted(by_drug.items(), key=lambda x: -x[1])[:5]),
            "feedback_by_type": by_type,
        }


