# L10-3: EQUITY MONITORING & SUBGROUP EVALUATION
# =============================================================================

class EquityMonitor:
    """
    L10-3: Monitors CURANIQ output quality across demographic subgroups.

    Detects if the system performs significantly worse for:
    - Specific age groups (pediatric, geriatric)
    - Sex/gender
    - Language (EN vs RU vs UZ queries)
    - Condition categories (rare diseases vs common)

    Methodology: compare safety gate pass rates, confidence scores,
    and refusal rates across subgroups. Flag >10% disparity.
    """

    DISPARITY_THRESHOLD = 0.10  # 10% difference = flagged

    def __init__(self):
        self._subgroup_metrics: dict[str, list[dict]] = {}

    def record(self, subgroup: str, category: str, metric: str, value: float):
        key = f"{subgroup}:{category}"
        self._subgroup_metrics.setdefault(key, []).append({"metric": metric, "value": value})

    def check_disparity(self) -> list[dict]:
        disparities = []
        by_metric: dict[str, dict[str, list[float]]] = {}

        for key, records in self._subgroup_metrics.items():
            subgroup, category = key.split(":", 1)
            for r in records:
                metric = r["metric"]
                by_metric.setdefault(metric, {}).setdefault(subgroup, []).append(r["value"])

        for metric, subgroups in by_metric.items():
            avgs = {sg: sum(vals) / len(vals) for sg, vals in subgroups.items() if vals}
            if len(avgs) < 2:
                continue
            max_val = max(avgs.values())
            min_val = min(avgs.values())
            if max_val > 0 and (max_val - min_val) / max_val > self.DISPARITY_THRESHOLD:
                best = max(avgs, key=avgs.get)
                worst = min(avgs, key=avgs.get)
                disparities.append({
                    "metric": metric,
                    "best_subgroup": best, "best_value": round(avgs[best], 3),
                    "worst_subgroup": worst, "worst_value": round(avgs[worst], 3),
                    "disparity": round((avgs[best] - avgs[worst]) / avgs[best], 3),
                })

        return disparities


