# L10-9: MULTILINGUAL CLINICAL EVALUATION HARNESS
# =============================================================================

class MultilingualEvalHarness:
    """
    L10-9: Tests CURANIQ accuracy across EN/RU/UZ languages.

    Runs the same clinical test cases in all supported languages
    and compares: refusal rates, safety gate pass rates, confidence
    scores, and output quality. Flags disparities.

    Connected to L8-5 Multilingual Interface for translation.
    """

    def generate_multilingual_cases(self, base_cases: list[dict]) -> list[dict]:
        """Generate test cases in all supported languages."""
        languages = ["en", "ru", "uz"]
        multilingual = []
        for case in base_cases:
            for lang in languages:
                multilingual.append({
                    **case,
                    "language": lang,
                    "original_language": "en",
                    "test_id": f"{case.get('case_id', 'unknown')}_{lang}",
                })
        return multilingual

    def compare_results(self, results: list[dict]) -> dict:
        """Compare results across languages for the same test case."""
        by_case: dict[str, dict[str, dict]] = {}
        for r in results:
            base_id = r.get("test_id", "").rsplit("_", 1)[0]
            lang = r.get("language", "en")
            by_case.setdefault(base_id, {})[lang] = r

        disparities = []
        for case_id, lang_results in by_case.items():
            if len(lang_results) < 2:
                continue
            confidences = {lang: r.get("confidence", 0) for lang, r in lang_results.items()}
            refusals = {lang: r.get("refused", False) for lang, r in lang_results.items()}

            # Check confidence disparity
            if confidences:
                max_c = max(confidences.values())
                min_c = min(confidences.values())
                if max_c > 0 and (max_c - min_c) / max_c > 0.2:
                    disparities.append({
                        "case_id": case_id,
                        "type": "confidence_disparity",
                        "detail": confidences,
                    })

            # Check refusal disparity (refused in one language but not another)
            if len(set(refusals.values())) > 1:
                disparities.append({
                    "case_id": case_id,
                    "type": "refusal_disparity",
                    "detail": refusals,
                })

        return {
            "total_cases": len(by_case),
            "disparities_found": len(disparities),
            "disparities": disparities[:20],
        }
