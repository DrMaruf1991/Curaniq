class EvidenceChangeImpactAnalyzer:
    """
    L10-6: When evidence updates, determines which past outputs are affected.

    When L1-16 detects new evidence (guideline update, retraction, new RCT),
    this module traces backward: "Which past CURANIQ responses used the
    now-outdated evidence? Which clinician decisions may need revisiting?"

    Connected to: L9-1 Ledger (past outputs), L8-6 Watchlist (notifications).
    """

    def analyze_impact(self, changed_evidence_id: str,
                       change_type: str,
                       affected_query_ids: list[str]) -> dict:
        severity = "low"
        if change_type == "retraction":
            severity = "critical"
        elif change_type == "correction":
            severity = "high"
        elif change_type == "superseded":
            severity = "moderate"

        return {
            "changed_evidence_id": changed_evidence_id,
            "change_type": change_type,
            "severity": severity,
            "affected_queries": len(affected_query_ids),
            "action": {
                "retraction": "URGENT: Notify all clinicians who received outputs citing this evidence. Flag for re-review.",
                "correction": "Notify affected clinicians. Outputs may need revision.",
                "superseded": "Update evidence cache. New outputs will use updated source.",
                "new_evidence": "Routine update. Watchlist subscribers notified.",
            }.get(change_type, "Review required."),
            "query_ids": affected_query_ids[:50],  # Cap for performance
        }


