@dataclass
class UsageEvent:
    event_type: str  # "query", "feedback", "export", "calculator", "pathway"
    user_role: str
    timestamp: datetime = field(default_factory=lambda: datetime.now(timezone.utc))
    metadata: dict = field(default_factory=dict)
    duration_ms: float = 0.0


class ProductAnalytics:
    """
    L9-8: Usage telemetry for product decisions. NO PHI collected.

    Tracks: query volume, feature usage, response times, user roles,
    error rates, popular calculators/pathways.

    PHI protection: only aggregate counts and categories. No query text,
    no patient identifiers, no drug names from specific queries.
    """

    def __init__(self):
        self._events: list[UsageEvent] = []

    def track(self, event_type: str, user_role: str,
              duration_ms: float = 0.0, metadata: dict = None) -> UsageEvent:
        event = UsageEvent(
            event_type=event_type, user_role=user_role,
            duration_ms=duration_ms, metadata=metadata or {},
        )
        self._events.append(event)
        return event

    def get_summary(self, hours: int = 24) -> dict:
        cutoff = datetime.now(timezone.utc).timestamp() - hours * 3600
        recent = [e for e in self._events if e.timestamp.timestamp() > cutoff]

        by_type: dict[str, int] = {}
        by_role: dict[str, int] = {}
        durations: list[float] = []
        for e in recent:
            by_type[e.event_type] = by_type.get(e.event_type, 0) + 1
            by_role[e.user_role] = by_role.get(e.user_role, 0) + 1
            if e.duration_ms > 0:
                durations.append(e.duration_ms)

        return {
            "period_hours": hours,
            "total_events": len(recent),
            "by_event_type": by_type,
            "by_user_role": by_role,
            "avg_duration_ms": round(sum(durations) / len(durations), 1) if durations else 0,
            "p95_duration_ms": round(sorted(durations)[int(len(durations) * 0.95)] if durations else 0, 1),
        }


