class SIEMSeverity(str, Enum):
    CRITICAL = "critical"
    HIGH     = "high"
    MEDIUM   = "medium"
    LOW      = "low"
    INFO     = "info"


class SIEMIntegration:
    """
    L9-9: Security Information & Event Management integration.

    Sends structured security events to external SIEM (Splunk, ELK, Sentinel)
    via webhook. Events include: prompt injection attempts, PHI leak attempts,
    authentication failures, safety gate overrides, rate limit breaches.

    Webhook URL from env: CURANIQ_SIEM_WEBHOOK_URL
    """

    def __init__(self):
        self._webhook_url = os.environ.get("CURANIQ_SIEM_WEBHOOK_URL", "")
        self._buffer: list[dict] = []

    def emit(self, event_type: str, severity: SIEMSeverity,
             details: dict, source_module: str = "") -> dict:
        event = {
            "event_id": str(uuid4()),
            "timestamp": datetime.now(timezone.utc).isoformat(),
            "event_type": event_type,
            "severity": severity.value,
            "source": f"CURANIQ:{source_module}",
            "details": details,
        }
        self._buffer.append(event)

        if severity in (SIEMSeverity.CRITICAL, SIEMSeverity.HIGH):
            logger.warning("SIEM [%s] %s: %s", severity.value, event_type, json.dumps(details)[:200])

        if self._webhook_url:
            self._send_webhook(event)

        return event

    def _send_webhook(self, event: dict):
        import urllib.request
        try:
            data = json.dumps(event).encode()
            req = urllib.request.Request(
                self._webhook_url, data=data,
                headers={"Content-Type": "application/json"},
                method="POST",
            )
            urllib.request.urlopen(req, timeout=5)
        except Exception as e:
            logger.warning("SIEM webhook failed: %s", e)

    def get_buffer(self) -> list[dict]:
        return list(self._buffer)


