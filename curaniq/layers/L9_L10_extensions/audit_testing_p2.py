class OverrideAction(str, Enum):
    ACCEPTED        = "accepted"
    DOSE_MODIFIED   = "dose_modified"
    DRUG_CHANGED    = "drug_changed"
    REJECTED        = "rejected"
    SAFETY_OVERRIDDEN = "safety_overridden"


@dataclass
class OverrideLogEntry:
    entry_id: str = field(default_factory=lambda: str(uuid4()))
    query_id: str = ""
    clinician_id: str = ""
    action: OverrideAction = OverrideAction.ACCEPTED
    original_recommendation: str = ""
    clinician_decision: str = ""
    override_reason: str = ""
    safety_gate_overridden: Optional[str] = None
    timestamp: datetime = field(default_factory=lambda: datetime.now(timezone.utc))
    signature: str = ""


class ClinicianOverrideLogger:
    """
    L9-4: Immutable audit trail for clinician overrides.

    Every time a clinician modifies, rejects, or overrides a safety gate,
    this logger creates a signed, immutable record. Connected to:
    - L9-1 Evidence Ledger (immutable storage)
    - L10-11 Clinician Trust Dashboard (pattern analysis)

    Safety overrides require explicit reason and are flagged for review.
    """

    def __init__(self):
        self._log: list[OverrideLogEntry] = []
        self._secret = os.environ.get("CURANIQ_AUDIT_SECRET", "").encode()

    def log_override(self, query_id: str, clinician_id: str,
                     action: OverrideAction, original: str,
                     decision: str, reason: str = "",
                     safety_gate: Optional[str] = None) -> OverrideLogEntry:
        entry = OverrideLogEntry(
            query_id=query_id, clinician_id=clinician_id,
            action=action, original_recommendation=original,
            clinician_decision=decision, override_reason=reason,
            safety_gate_overridden=safety_gate,
        )

        content = f"{entry.entry_id}|{query_id}|{clinician_id}|{action.value}|{entry.timestamp.isoformat()}"
        if self._secret:
            entry.signature = hmac.new(self._secret, content.encode(), hashlib.sha256).hexdigest()

        self._log.append(entry)

        if action == OverrideAction.SAFETY_OVERRIDDEN:
            logger.warning("SAFETY OVERRIDE: clinician=%s query=%s gate=%s reason=%s",
                          clinician_id, query_id, safety_gate, reason[:100])

        return entry

    def get_safety_overrides(self) -> list[OverrideLogEntry]:
        return [e for e in self._log if e.action == OverrideAction.SAFETY_OVERRIDDEN]


